inline f32
Sin(f32 Angle)
{
    return sinf(Angle);
}

inline f32
Cos(f32 Angle)
{
    return cosf(Angle);
}

inline f32
Atan2F32(f32 Y, f32 X)
{
    return (f32)atan2(Y, X);
}

inline f64
Atan2F64(f64 Y, f64 X)
{
    return atan2(Y, X);
}

inline f32
AcosF32(f32 Arc)
{
    return (f32)acos(Arc);
}

inline f64
AcosF64(f64 Arc)
{
    return acos(Arc);
}